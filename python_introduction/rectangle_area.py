# Task 2: Calculate the Area of a Rectangle
# Objective: Calculate the area of a rectangle using its length and width.

# --- Part A: Define the variables for length and width ---
# We are setting up the given values for the rectangle's dimensions.
length = 10  # The length of the rectangle
width = 5    # The width of the rectangle

# --- Part B: Calculate the area of the rectangle ---
# The formula for the area of a rectangle is Length multiplied by Width.
# The '*' symbol is used for multiplication in Python.
area = length * width

# --- Part C: Print the calculated area in the specified format ---
# We use an f-string to display the result clearly, embedding the 'area' variable.
print(f"The area of the rectangle is: {area}")